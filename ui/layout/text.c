static font_t default_font;

static void text_draw_glyph(uint32_t x, uint32_t y, const font_glyph_t *glyph, uint32_t fg, uint32_t bg, uint32_t scale) {
    if (!glyph->data || scale == 0)
        return;
    
    for (uint32_t row = 0; row < glyph->height; row++) {
        for (uint32_t col = 0; col < glyph->width; col++) {
            uint32_t byte_idx = row * ((glyph->width + 7) / 8) + col / 8;
            uint8_t bits = glyph->data[byte_idx];
            bool pixel = (bits & (0x80 >> (col % 8))) != 0;
            
            if (pixel) {
                for (uint32_t sy = 0; sy < scale; sy++) {
                    for (uint32_t sx = 0; sx < scale; sx++) {
                        fb_put_pixel(x + col * scale + sx, y + row * scale + sy, fg);
                    }
                }
            } else if (bg != 0xFFFFFFFF) {
                for (uint32_t sy = 0; sy < scale; sy++) {
                    for (uint32_t sx = 0; sx < scale; sx++) {
                        fb_put_pixel(x + col * scale + sx, y + row * scale + sy, bg);
                    }
                }
            }
        }
    }
}

void text_init(void) {
    default_font.name = "default";
    default_font.default_width = 8;
    default_font.default_height = 16;
    
    for (int i = 0; i < 96; i++) {
        default_font.glyphs[i].width = 8;
        default_font.glyphs[i].height = 16;
        default_font.glyphs[i].data = default_font_data[i];
    }
    
    text_set_font(&default_font);
}

void text_draw_char(uint32_t x, uint32_t y, char c, uint32_t fg, uint32_t bg) {
    text_draw_char_scaled(x, y, c, fg, bg, 1);
}

void text_draw_char_scaled(uint32_t x, uint32_t y, char c, uint32_t fg, uint32_t bg, uint32_t scale) {
    if (c < 32 || c > 127)
        return;
    
    const font_glyph_t *glyph = &current_font->glyphs[c - 32];
    text_draw_glyph(x, y, glyph, fg, bg, scale);
}

void text_draw(uint32_t x, uint32_t y, const char *str, uint32_t fg, uint32_t bg) {
    text_draw_scaled(x, y, str, fg, bg, 1);
}

void text_draw_scaled(uint32_t x, uint32_t y, const char *str, uint32_t fg, uint32_t bg, uint32_t scale) {
    uint32_t cx = x;
    uint32_t cy = y;
    for (int i = 0; str[i]; i++) {
        if (str[i] == '\n') {
            cx = x;
            cy += current_font_height * scale;
            continue;
        }
        text_draw_char_scaled(cx, cy, str[i], fg, bg, scale);
        cx += current_font_width * scale;
    }
}

void text_draw_wrapped(uint32_t x, uint32_t y, uint32_t max_width, const char *str, uint32_t fg, uint32_t bg) {
    uint32_t cx = x;
    uint32_t cy = y;
    uint32_t scale = 1;
    uint32_t char_width = current_font_width * scale;
    
    for (int i = 0; str[i]; i++) {
        if (str[i] == '\n') {
            cx = x;
            cy += current_font_height * scale;
            continue;
        }
        
        if (cx + char_width > x + max_width) {
            cx = x;
            cy += current_font_height * scale;
        }
        
        text_draw_char_scaled(cx, cy, str[i], fg, bg, scale);
        cx += char_width;
    }
}

size_t text_width(const char *str) {
    if (!str) return 0;
    size_t n = 0;
    while (str[n] && str[n] != '\n') n++;
    return n * current_font_width;
}

size_t text_width_char(char c) {
    if (c < 32 || c > 127)
        return 0;
    return current_font_width;
}

size_t text_height(void) {
    return current_font_height;
}

void text_set_font(const font_t *font) {
    if (font) {
        current_font = font;
        current_font_width = font->default_width;
        current_font_height = font->default_height;
    }
}

const font_t *text_get_font(void) {
    return current_font;
}